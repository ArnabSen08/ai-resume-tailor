// Simple PDF generation using browser's print functionality
function generatePDF() {
    const resumeContent = document.getElementById('tailoredResume').textContent;
    
    if (!resumeContent) {
        showError('No resume content to generate PDF.');
        return;
    }
    
    // Create a new window with the resume content
    const printWindow = window.open('', '_blank');
    
    const htmlContent = `
        <!DOCTYPE html>
        <html>
        <head>
            <title>Tailored Resume</title>
            <style>
                body {
                    font-family: 'Times New Roman', serif;
                    line-height: 1.6;
                    margin: 40px;
                    color: #333;
                }
                .header {
                    text-align: center;
                    margin-bottom: 30px;
                    border-bottom: 2px solid #333;
                    padding-bottom: 20px;
                }
                .content {
                    white-space: pre-wrap;
                    font-size: 12px;
                }
                @media print {
                    body { margin: 20px; }
                    .no-print { display: none; }
                }
            </style>
        </head>
        <body>
            <div class="header">
                <h1>Tailored Resume</h1>
                <p>Generated by AI Resume Tailor</p>
            </div>
            <div class="content">${resumeContent}</div>
            <script>
                window.onload = function() {
                    window.print();
                    window.onafterprint = function() {
                        window.close();
                    }
                }
            </script>
        </body>
        </html>
    `;
    
    printWindow.document.write(htmlContent);
    printWindow.document.close();
}

